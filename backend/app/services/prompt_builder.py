def build_news_prompt(text: str) -> str:
    return f"""
역할:
- 너는 한국어 뉴스 요약 전용 AI다.

규칙:
- 한국어만 출력
- 원문에 없는 내용 금지
- 고유명사/수치/연도는 원문 그대로 유지
- 반드시 불릿 5줄만 출력 (각 줄 1문장)
- 5번째 불릿을 작성한 다음, 다음 줄에 <END>만 출력하고 즉시 종료
- 출력은 오직 불릿 5줄 + 마지막 줄 <END> 만 허용한다.
- 불릿은 반드시 '-'로 시작하고, 정확히 5줄만 출력한다.
- 각 불릿은 한 문장(마침표로 끝).
- 원문에 없는 내용은 절대 추가하지 마라.
- 5번째 불릿을 출력한 직후, 다음 줄에 <END>만 출력하고 즉시 종료한다.
- <END> 이후 어떤 글자도 출력하지 마라. (공백/줄바꿈 추가도 금지)
- 서두/인사/추가 설명 금지

출력 형식(반드시 그대로):
- ...
- ...
- ...
- ...
- ...
<END>

[기사 원문]
{text}
""".strip()


def build_prompt(text: str, mode: str) -> str:
    if mode == "news":
        return build_news_prompt(text)
    if mode == "report":
        return f"아래 내용을 한국어로 구조화 요약해라.\n\n[원문]\n{text}"
    return f"아래 텍스트를 한국어로 5줄 이내로 요약하라.\n\n[원문]\n{text}"
